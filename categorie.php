    <script>
        function sendMessage() {
            const chatMessages = document.getElementById('chatMessages');
            const messageInput = document.getElementById('messageInput');

            const messageText = messageInput.value;
            if (messageText.trim() === '') return;

            // Ajout du message de l'artisan
            const artisanMessage = document.createElement('div');
            artisanMessage.className = 'message client';
            artisanMessage.innerHTML = `<strong>Client :</strong> ${messageText}`;
            chatMessages.appendChild(artisanMessage);

            // Réinitialiser le champ d'entrée
            messageInput.value = '';

            // Simuler la réponse de l'administration après une petite pause
            setTimeout(() => {
                const administrationMessage = document.createElement('div');
                administrationMessage.className = 'message artisan';
                administrationMessage.innerHTML = '<strong>Artisan:</strong> Merci pour votre message. Que puis-je faire pour vous ?';
                chatMessages.appendChild(administrationMessage);

                // Faire défiler vers le bas
                chatMessages.scrollTop = chatMessages.scrollHeight;
            }, 1000);

            // Faire défiler vers le bas
            chatMessages.scrollTop = chatMessages.scrollHeight;
        }

        function toggleChat() {
            const chatContainer = document.getElementById('chatContainer');
            if (chatContainer.style.display === 'none' || chatContainer.style.display === '') {
                chatContainer.style.display = 'block';
            } else {
                chatContainer.style.display = 'none';
            }
        }
    </script>

